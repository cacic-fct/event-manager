import { BadRequestException, Controller, HttpCode, Param, Post, RawBodyRequest, Req } from '@nestjs/common';
import {
  ApiBadRequestResponse,
  ApiBody,
  ApiConsumes,
  ApiOperation,
  ApiParam,
  ApiResponse,
  ApiTags,
} from '@nestjs/swagger';
import type { Request } from 'express';

import { AnalyticsService } from './analytics.service';
import { SENTRY_TUNNEL_TARGETS } from './analytics.config';

@ApiTags('Analytics')
@Controller('a')
export class AnalyticsController {
  constructor(private readonly analyticsService: AnalyticsService) {}

  @Post('glitchtip/:project')
  @HttpCode(200)
  @ApiOperation({
    summary: 'Forward Sentry envelope events',
    description:
      'Receives Sentry envelope payloads from frontend SDK tunnel requests and forwards them to the configured GlitchTip project. ' +
      'This endpoint is intended to avoid ad blockers blocking direct browser requests to Sentry envelope URLs.',
  })
  @ApiParam({
    name: 'project',
    description: 'Configured monitoring project key. Must match one of the keys in SENTRY_TUNNEL_TARGETS.',
    enum: Object.keys(SENTRY_TUNNEL_TARGETS),
    example: Object.keys(SENTRY_TUNNEL_TARGETS)[0],
  })
  @ApiConsumes('application/x-sentry-envelope', 'text/plain')
  @ApiBody({
    required: true,
    description:
      'Raw Sentry envelope payload generated by the Sentry JavaScript SDK. The body must be forwarded unchanged.',
    schema: {
      type: 'string',
      format: 'binary',
      example:
        '{"event_id":"...","dsn":"https://PUBLIC_KEY@glitchtip.cacic.dev.br/ID"}\n{"type":"event"}\n{"message":"Example error"}',
    },
  })
  @ApiResponse({
    status: 200,
    description: 'Envelope accepted by the backend tunnel and forwarded to GlitchTip.',
  })
  @ApiBadRequestResponse({
    description: 'Unknown monitoring project or malformed request.',
  })
  async tunnel(@Param('project') project: string, @Req() req: RawBodyRequest<Request>): Promise<void> {
    if (!(project in SENTRY_TUNNEL_TARGETS)) {
      throw new BadRequestException('Unknown monitoring project');
    }

    await this.analyticsService.forwardEnvelope(project, req);
  }
}
